public class Sorted {
    public static void main(String[] args) {
        int [] arr= {1,54,67,89};
        for(int i =0;i<arr.length;i++)
        {
            if(arr[i]>arr[i+1])
            {
                System.out.println("not sorted");
                break;
            }
            else
            {
                System.out.println("not sorted");
                break;
            }
        }
    }
    
}
